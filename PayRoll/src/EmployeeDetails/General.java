    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int i = JOptionPane.showConfirmDialog(jPanel1, "Are you sure you want to save changes?", "Save changes", JOptionPane.YES_NO_CANCEL_OPTION);
        if (i == JOptionPane.YES_OPTION) {
            int empn = Integer.parseInt(empno.getText());
            String fnameSt = fname.getText();
            String lnameSt = lname.getText();
            String genSt = gender.getSelectedItem().toString();
            String dobSt = dob.getText();
            String docSt = doc.getText();
            int desg = jComboBox1.getSelectedIndex();
            BigDecimal bas = new BigDecimal(bassalry.getText());
            String ni = nic.getText();
            String off_day = offday.getSelectedItem().toString();
            int tele = Integer.parseInt(tel.getText());
            boolean b = db.updateDEmployee(empn, fnameSt, lnameSt, genSt, dobSt, ni, tele, desg, docSt, bas.intValue(), off_day);
            if(b){
            JOptionPane.showMessageDialog(jPanel1, "Updates saved succesfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

