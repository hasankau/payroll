    private void setEmpData(Employee e) {
        try {
            empno.setText(e.getEmpno());
            bassalry.setText(e.getBasicsalary() + "");
            fname.setText(e.getFirstname());
            lname.setText(e.getLastnme());
            nic.setText(e.getNic());
            tel.setText(e.getTel());
            offday.setSelectedItem(e.getOffday());
            gender.setSelectedItem(e.getGender());
            jComboBox1.setSelectedIndex(Integer.parseInt(e.getDesignation()));

            Calendar dobCal = Calendar.getInstance();
            dobCal.setTime(sdf.parse(e.getDob()));
            dob.setSelectedDate(dobCal);
            
            Calendar docCal = Calendar.getInstance();
            dobCal.setTime(sdf.parse(e.getDoc()));
            System.out.println(e.getDoc());
            doc.setSelectedDate(docCal);
            
        } catch (Exception exc) {
            exc.printStackTrace();
        }
    }

