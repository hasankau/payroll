    private void homepaneSetting() {
        File f = new File("src/settings.ini");
        if (f.exists()) {

            try {
                BufferedReader bf = new BufferedReader(new FileReader(f));
                String line = bf.readLine();
                if (line != null) {
                    String[] lines = line.split("=");

                    if ("on".equals(lines[1])) {
                        homeset = true;
                        cardHolder.add(new HomePane(this));
                    } else {
                        homeset = false;
                        cardHolder.add(newemp);
                    }

                } else {
                    homeset = false;
                    cardHolder.add(newemp);
                }
            } catch (IOException ex) {
                Logger.getLogger(HomePane.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            cardHolder.add(new HomePane(this));
        }
    }

