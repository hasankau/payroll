    private void liempActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_liempActionPerformed
        //NewEmployee ne = (NewEmployee) cardHolder.getComponent(0);
        newemp.doneHere();
        if (LeavePage.getInstance().doneHere) {
            cardHolder.removeAll();
            cardHolder.add(new ListEmployee());
            cardHolder.repaint();
            cardHolder.revalidate();
        } else {
            int i = JOptionPane.showOptionDialog(null, "There are unsaved changes. "
                    + "If you continue without saving,"
                    + " \n the data will be lost. Do you wish to continue?", "Unsaved Changes", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE, null, null, rootPane);
            if (i == JOptionPane.YES_OPTION) {
                listEmployee();
                LeavePage.getInstance().doneHere = true;
                newemp.clearFields();
                newemp.clearTable();
            }
        }

    }//GEN-LAST:event_liempActionPerformed

